#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dup;
	int		i;

	dup = (char*)malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (dup == NULL)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
