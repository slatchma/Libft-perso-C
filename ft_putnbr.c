#include "libft.h"

void	ft_putnbr(int nb)
{
	int i;

	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		nb = 147483648;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = nb * -1;
	}
	i = nb % 10;
	if (nb / 10 > 0)
	{
		ft_putnbr(nb / 10);
	}
	if (nb != -2147483648)
	{
		ft_putchar(i + 48);
	}
}
